<div class="feature-aria">
<div class="feature-content">
<h3 class="iq-font-dark iq-tw-6 iq-mb-10 iq-re-9-mt30">La société à responsabilité limitée à associé unique(SARL-AU)</h3>
<p>Entreprise ne comptant qu’un seul associé. L’entrepreneur bénéficie du régime de la responsabilité limitée et conserve l’entière maîtrise de ses affaires. Un capital social doit être constitué.</p>

