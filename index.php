<div class="feature-aria">
<div class="feature-content">
<h3 class="iq-font-dark iq-tw-6 iq-mb-10 iq-re-9-mt30">La société à responsabilité limitée (SARL)</h3>

<p>La SARL, type de société le plus couramment utilisé dans le cadre de la création d'entreprise, offre l'avantage d'une structure simple au sein de laquelle la responsabilité des associés est limitée au montant de leurs apports.
Son capital, dont la loi ne fixe aucun montant minimal, est réparti entre au moins deux associés. Elle est dirigée par un ou plusieurs gérants, associés ou non.</p>

</div>
</div>
</div>
