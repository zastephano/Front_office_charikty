<div class="feature-aria">
<div class="feature-content">
<h3 class="iq-font-dark iq-tw-6 iq-mb-10 iq-re-9-mt30">La société de personnes</h3>
<p>Est société constituée par des associés ayant une forte relation entre eux, soit parce que leurs intérêts sont intimement liés, soit parce qu’ils collaborent effectivement et personnellement à la poursuite du but social. Dans ce type de société, les cessions de parts sociales sont généralement soumises à accord préalable des autres associés.</p>

</div>
</div>
</div>
</div>
</div>
<div class="row iq-mt-50">
<div class="col-lg-6 col-md-12">
<div class="iq-feature7 wow fadeInLeft" data-wow-duration="1.5s" style="visibility: visible; animation-duration: 1.5s; animation-name: fadeInLeft;">
<div class="feature-aria">
<div class="feature-content">
<h3 class="iq-font-dark iq-tw-6 iq-mb-10 iq-re-9-mt30">La société Anonyme (SA)</h3>

<p>La société anonyme est une société commerciale dans laquelle les associés, dénommés actionnaires en raison
d’un droit représenté par un titre négociable ou action, ne supportent les dettes sociales qu’à concurrence de
leurs apports. Les principales caractéristiques de la SA sont les suivantes : </p>
<ul>
<li>Le nombre d’actionnaires ne peut être inférieur à 5;</li>
<li>Le capital minimum est de 3 millions de dhs pour les SA, faisant appel à l’épargne publique et 300.000,00 dhs dans le cas contraire;</li>
<li>Les actionnaires ne sont responsables qu’à concurrence de leurs apports;</li>
<li>Les actions sont représentées par des titres librement négociables;</li>
<li>Les actions de numéraire doivent êtres libérées lors de la souscription d’au moins le ¼ de leur valeur nominale;</li>
<li>Le capital doit être intégralement souscrit, à défaut la société ne peut être constituée.</li>
</ul>
